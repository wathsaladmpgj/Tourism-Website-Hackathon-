        It marks the moment Sri Lanka embraced Buddhism as a way of life, which greatly influenced its culture and traditions.
        Devotees visit temples for prayers and rituals. Mihintale, believed to be the site of Arahat Mahinda's arrival, becomes a focal point for pilgrims. Acts of generosity, such as offering free meals (Dansals), are common.

